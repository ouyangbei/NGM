     <p style="font-size:16px">移动互联网(MobileInternet, 简称MI)是一种通过智能移动终端，采用移动无线通信方式获取业务和服务的新兴业务，包含终端、软件和应用三个层面。终端层包括智能手机、平板电脑、电子书、MID等；软件包括操作系统、中间件、数据库和安全软件等。应用层包括休闲娱乐类、工具媒体类、商务财经类等不同应用与服务。随着技术和产业的发展，未来，LTE(长期演进，4G通信技术标准之一)和NFC(近场通信，移动支付的支撑技术)等网络传输层关键技术也将被纳入移动互联网的范畴之内。
